<?php 

// Arithmetic operators
$age = 37; // initial value
$age += 1; // $age = $age + 1;

echo 'Your age now is ' . $age;

$age -= 1; // $age = $age - 1;

echo "\nYour age now is " . $age;

$age /= 2; // $age = $age / 2;

echo "\nYour age now is " . $age;

$age *= 3; // $age = $age * 3;

echo "\nYour age now is " . $age;
